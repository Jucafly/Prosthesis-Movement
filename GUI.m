% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)

global L1 L2 L4 L5 L6
L1 = 30; L2 = 418; L4 = 78.818; L5 = 64.414; L6 = 173.5;

%GetText
TYF = str2double(get(handles.edit2,'String'));
TXF = str2double(get(handles.edit3,'String'));
Rodi = str2double(get(handles.edit4,'String'));
Pris = str2double(get(handles.edit5,'String'));

q1 =  deg2rad(Rodi);           %rodilla
q2 =  deg2rad(TXF);            %
d3 =  deg2rad(Pris);             %prismatico
q4 =  deg2rad(TYF);              %tobillo Y
X = -sin(q2)*(d3 + L6*cos(q4) - L5*sin(q4));
Y = L5*(cos(q4)*sin(q1) - cos(q1)*cos(q2)*sin(q4)) + L6*(sin(q1)*sin(q4) + cos(q1)*cos(q2)*cos(q4)) + L2*sin(q1) + L4*sin(q1) + d3*cos(q1)*cos(q2);
Z = d3*cos(q2)*sin(q1) - L5*(cos(q1)*cos(q4) + cos(q2)*sin(q1)*sin(q4)) - L6*(cos(q1)*sin(q4) - cos(q2)*cos(q4)*sin(q1)) - L2*cos(q1) - L4*cos(q1) - L1 ;

set(handles.edit9,'String',X);
set(handles.edit10,'String',Y);
set(handles.edit11,'String',Z);

%handles.axes2=plot3([0;X],[0;Y],[0;Z],'r');

%Assignacion
assignin('base','TYF',TYF);
assignin('base','TXF',TXF);
assignin('base','Rodi',Rodi);
assignin('base','Pris',Pris);

pause(1);
%Empezar Programa
set_param(gcs,'SimulationCommand','Start');



